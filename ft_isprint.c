int ft_isgraph (int c)
{
	if (c > ' ' && c < 127)
		return (1);
	else
		return (0);
}

int ft_isprint (int c)
{
	if (ft_isgraph(c) || c == ' ')
		return (1);
	else
		return (0);
}
